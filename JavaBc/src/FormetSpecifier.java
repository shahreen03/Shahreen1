public class FormetSpecifier {
    public static void main(String[] args) {
        int myint= 10;
        char mychar = 'c';
       String mystring= "Shahreen Jahan";
        float myfloat = 10.50f;
        double mydouble = 10.9999;
        boolean mybool= true;

        System.out.printf("Int = %d\n", myint);
        System.out.printf("Char = %c\n", mychar);
        System.out.printf("String = %s\n", mystring);
        System.out.printf("Float = %f\n", myfloat);
        System.out.printf("Double = %f\n", mydouble);
        System.out.printf("Boolean = %b\n", mybool);
        /*s – formats strings
          d – formats decimal integers
          f – formats the floating-point numbers
          t– formats date/time values*/
    }
}
