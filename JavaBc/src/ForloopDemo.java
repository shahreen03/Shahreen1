public class ForloopDemo {
    public static void main(String[] args) {
       for(int i = 1; i<= 10; i++){
            System.out.println(i + ". Bangladesh.");
        }
        for( int i = 10; i >=0; i-- ){
            System.out.println(i);
        }
        for(int i = 2; i<=100; i=i+2){
            System.out.println( i );
            }
        for(int i = 1; i <= 99; i = i+2){
            System.out.println(i);
        }
    }
}
